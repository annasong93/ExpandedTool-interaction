   $(window).on('scroll', function () {
        var scrollTop = $(window).scrollTop();
        if (scrollTop > 700) {
            $('#head').stop().animate({height: "0px"});
            $('#title').hide();
        }
        else {
             $('#head').stop().animate({height: "100vw"},1000); 
             $('#title').show(1000);
        }
     });


